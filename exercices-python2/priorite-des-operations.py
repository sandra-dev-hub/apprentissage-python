# 1. Prédiction du résultat
# L'ordinateur suit la règle PEMDAS (Parenthèses, Exposants, Multiplication/Division, Addition/Soustraction)
resultat_par_defaut = 5 + 3 * 2
print(f"Résultat sans parenthèses (3*2 est fait en premier) : {resultat_par_defaut}") 

# 2. Modification avec parenthèses
resultat_modifie = (5 + 3) * 2
print(f"Résultat avec parenthèses (5+3 est fait en premier) : {resultat_modifie}")    
